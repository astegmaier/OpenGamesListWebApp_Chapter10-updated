import { AbstractControl } from '@angular/common';

export class ValidatorService {
    static email(control: AbstractControl) {
        return (control.value.match(/[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/))
            ? null
            : { 'invalidEmail': true };
    }
}